    # ── Read: Search ───────────────────────────────────────────────────────────

    def search_events(self, query: str, limit: int = 100) -> List[dict]:
        try:
            conn = self._connect()
            try:
                # Basic LIKE search on payload for simplicity
                rows = conn.execute(
                    """
                    SELECT * FROM raw_events
                    WHERE payload LIKE ?
                    ORDER BY id DESC
                    LIMIT ?
                    """,
                    (f"%{query}%", limit)
                ).fetchall()
                
                result = []
                for r in rows:
                    d = dict(r)
                    try:
                        d["payload"] = json.loads(d["payload"])
                    except Exception: pass
                    result.append(d)
                return result
            finally:
                conn.close()
        except Exception as e:
            logger.error("[EventStore] search_events failed: %s", e)
            return []

    # ── Read: MITRE Heatmap ────────────────────────────────────────────────────

    def get_mitre_hits(self) -> List[dict]:
        """Returns MITRE technique hit counts from stored alerts."""
        try:
            conn = self._connect()
            try:
                rows = conn.execute(
                    "SELECT mitre_data FROM alerts WHERE suppressed=0 AND mitre_data IS NOT NULL"
                ).fetchall()
            finally:
                conn.close()

            counts: Dict[str, int] = {}
            for row in rows:
                try:
                    items = json.loads(row["mitre_data"])
                    for item in items:
                        tid = item.get("id") or item.get("technique_id") or "Unknown"
                        counts[tid] = counts.get(tid, 0) + 1
                except Exception:
                    pass

            return [{"technique": k, "count": v}
                    for k, v in sorted(counts.items(), key=lambda x: -x[1])]
        except Exception as e:
            logger.error("[EventStore] get_mitre_hits failed: %s", e)
            return []


