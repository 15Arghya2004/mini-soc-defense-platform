    # ── Incident Writes & Reads ────────────────────────────────────────────────

    def store_incident(self, incident: dict):
        try:
            conn = self._connect()
            conn.execute(
                """
                INSERT INTO incidents (incident_id, source_ip, severity, risk_score, sources, alert_count, status, payload)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?)
                ON CONFLICT(incident_id) DO UPDATE SET
                    alert_count = excluded.alert_count,
                    risk_score = excluded.risk_score,
                    severity = excluded.severity,
                    sources = excluded.sources,
                    payload = excluded.payload
                """,
                (
                    incident.get("incident_id") or f"inc-{incident.get('source_ip')}-{int(time.time())}",
                    incident.get("source_ip"),
                    incident.get("severity", "medium"),
                    float(incident.get("risk_score", 0)),
                    json.dumps(incident.get("sources", [])),
                    int(incident.get("alert_count", 0)),
                    incident.get("status", "new"),
                    json.dumps(incident)
                )
            )
            conn.commit()
            conn.close()
        except Exception as e:
            logger.error("[EventStore] store_incident failed: %s", e)

    def get_incidents(self, limit: int = 100, offset: int = 0) -> List[dict]:
        try:
            conn = self._connect()
            rows = conn.execute("SELECT * FROM incidents ORDER BY id DESC LIMIT ? OFFSET ?", (limit, offset)).fetchall()
            conn.close()
            res = []
            for r in rows:
                d = dict(r)
                if d.get("payload"):
                    d["payload"] = json.loads(d["payload"])
                if d.get("sources"):
                    d["sources"] = json.loads(d["sources"])
                res.append(d)
            return res
        except Exception as e:
            logger.error("[EventStore] get_incidents failed: %s", e)
            return []

